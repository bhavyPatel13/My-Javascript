let a = prompt("Enter number 0-6");
switch (a) {
  case 0:
    a = "Sunday";
    break;
  case 1:
    a = "Monday";
    break;
  case 2:
    a = "Tuesday";
    break;
  case 3:
    a = "Wednesday";
    break;
  case 4:
    a = "Thursday";
    break;
  case 5:
    a = "Friday";
    break;
  case 6:
    a = "Saturday";
}

console.log(a);
