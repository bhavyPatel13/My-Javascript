// div equal symbol is /=
// a /= 2 is equivalent to a = a / 2
// b /= 3 is equivalent to b = b / 3

let a = 10;
a /= 2; // a = a / 2
console.log("a =",a);

let b = 15;
b /= 3; // b = b / 3
console.log("b =",b);

let c = 20;
c = c / 4; // c /= 4
console.log("c =",c);

let d = 25;
d = d / 5; // d /= 5
console.log("d =",d);