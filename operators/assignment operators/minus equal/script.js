// minus equal symbol is -=
// a -= 2 is equivalent to a = a - 2
// b -= 5 is equivalent to b = b - 5

let a = 10;
a -= 2; // a = a - 2
console.log("a =",a);

let b = 20;
b -= 5; // b = b - 5
console.log("b =",b);

let c = 30;
c = c - 7 ; // c = c - 7
console.log("c =",c);

let d = 40;
d = d - 9; // d = d - 9
console.log("d =",d);