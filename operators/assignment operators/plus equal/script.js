// plus equal symbol is +=
// a += 1 ; is equivalent to a = a + 1;
// b += 4 ; is equivalent to b = b + 4;

let a = 5;
a += 4; // equivalent to a = a + 4;
console.log("a =",a); // a = 9

let b = 10;
b += 2; // equivalent to b = b + 2;
console.log("b =",b); // b = 12

let c = 15;
c = c + 3; // equivalent to c += 3;
console.log("c =",c); // c = 18

let d = 20;
d = d + 1; // equivalent to d += 1;
console.log("d =",d); // d = 21