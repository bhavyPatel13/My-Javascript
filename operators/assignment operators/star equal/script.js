// start equal symbol is *=
// a *= 5; is same as a = a * 5;
// b *= 7; is same as b = b * 7;

let a = 5;
a *= 5; // a = a * 5;
console.log("a =",a);

let b = 10;
b *= 7; // b = b * 7;
console.log("b =",b);

let c = 15;
c = c * 9; // c = c * 9;
console.log("c =",c);

let d = 20;
d = d * 11; // d = d * 11;
console.log("d =",d);