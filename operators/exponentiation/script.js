// exponentiation symbol is **
// a power of b = b time multiplication with a
// example : 2 power 2 = 2 * 2
// example : 2 ** 2 =  2 * 2 = 4

// example : 2 power 3 = 2 * 2 * 2
// example : 2 ** 3 = 2 * 2 * 2 = 8

// example : 3 power 4 = 3 * 3 * 3 * 3
// example : 3 ** 4 = 3 * 3 * 3 * 3 = 81

let a = 3;
let b = 4;
console.log("a ** b =", a ** b); // 81

let c = 4;
let d = 5;
console.log("c ** d =", c ** d); // 1024

let e = 2;
let f = 5;
let x = e ** f;
console.log("e ** f =", x); // 32

let g = 3;
let h = 6;
let y = g ** h;
console.log("g ** h =", y); // 729