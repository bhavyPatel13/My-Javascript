// decrement Symbol is --
// a-- = a - 1

let a = 5;
a -- // a = 5, a-- = 4
console.log("a =",a);
console.log("a-- =",a--);

let b = 8;
b -- // b = 8, b-- = 7
console.log("b =",b);
console.log("b-- =",b--);

let c = 13;
c = c - 1 // c = 12
console.log("c =",c);
console.log("c-- =",c--);

let d = 20;
d = d - 1 // d = 19
console.log("d =",d);
